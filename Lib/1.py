num1 = int(input("Введіть вартість ігрової приставки:"))
num2 = int(input("Введіть кількість ігрових приставок:"))
num3 = int(input("Введіть відсоток знижки:"))

if num3 == 0:
    print("Загальна сума замовлення:", num1 * num2)
else:
    print("Вартість однієї приставки з урахуванням знижки:", num1 - (num1 * num3 / 100))